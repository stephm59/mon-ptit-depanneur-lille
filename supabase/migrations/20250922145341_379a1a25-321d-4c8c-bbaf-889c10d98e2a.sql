## L''essentiel en 15 secondes

À faire chaque année (ou 2x/an si usage intensif été + hiver).

4 blocs clés : hygiène (filtres/évaporateurs), condensats (pas de fuites), froid (étanchéité/charges), élec/réglages (modes/consignes).

Signes d''alerte : odeur, eau qui goutte, moins de froid, bruit inhabituel.

À Lille / cœur d''îlot : soigner bruit et vibrations (silentblocs, châssis), surtout en copro.

## Quand faire l''entretien ?

**Usage été uniquement :** 1 passage au printemps.

**Clim réversible (chauffe mi-saison) :** printemps + début d''automne si possible.

**Après travaux/poussière :** entretien anticipé (filtres encrassés).

## Checklist d''un entretien pro (pas à pas)

### 1) Hygiène & qualité de l''air (UI – unités intérieures)

- Déclipser, dépoussiérer les filtres (ou remplacement si usés).
- Nettoyer/désinfecter évaporateur et turbine (produits compatibles).
- Vérifier l''état des bacs à condensats (algues/biofilm), sonde et pompe.
- Contrôler écoulement : pas de stagnation, pas d''odeur.

### 2) Condensats (UI + UE)

- Purger/ramoner la ligne de condensats ; vérifier pente et siphon.
- Tester la pompe de relevage si présente.
- À l''extérieur : s''assurer que l''écoulement ne gêne pas le voisinage.

### 3) Partie frigorifique

- Contrôle visuel étanchéité des liaisons, isolants et raccords.
- Relevé pressions/temperatures si nécessaire, contrôle surchauffe/sous-refroidissement (selon protocole).
- Vérifier l''état de l''échangeur extérieur (dégagement, ailettes redressées si besoin).

### 4) Électricité & sécurité

- Serrage des dominos/borniers, contrôle terre et protections.
- Vérif commutations (ventilateur, compresseur), sondes.
- Mise à jour firmware si système connecté.

### 5) Performance & réglages

- Test des modes (froid, chaud, déshumidification), vitesses.
- Conseils consignes (été 25–26 °C ; hivers mi-saison) et programmations.
- En milieu urbain : activer mode nuit et limiter les montées en régime nocturnes.

### 6) Acoustique & vibrations (spécial Lille / cœur d''îlot)

- Contrôler silentblocs et châssis de l''UE (pas de fissure/tassement).
- Vérifier orientation du souffle (loin fenêtres) et dégagement d''air.
- Proposer, si besoin, un écran absorbant correctement ventilé.

### 7) Rapport & recommandations

- Rapport avec photos, points faits, anomalies, recommandations (filtres, pompe, écran, réglages).
- Devis si pièce HS (pompe, carte, ventilateur).

## Ce que vous pouvez faire vous-même (entre deux visites)

- Aspirer les filtres toutes les 4–8 semaines (ou les remplacer si jetables).
- Laisser 2–3 m dégagés devant les unités intérieures.
- Éviter consignes extrêmes (pas 18 °C en plein été) → moins de conso, moins d''usure.
- Sur l''UE, vérifier visuellement qu''aucun déchet n''obstrue la grille.

## Signes d''alerte (appelez un pro)

Fuite d''eau sous l''UI, odeur persistante, baisse de froid/chaleur, bruit inhabituel, code erreur.

## Fréquence & durée

**1 entretien/an** (usage standard) ; **2 si usage réversible intensif**.

**Durée indicative :** 45–90 min selon le nombre d''unités et l''accessibilité.

