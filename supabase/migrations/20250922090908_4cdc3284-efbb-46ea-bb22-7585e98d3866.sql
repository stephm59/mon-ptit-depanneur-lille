## Spécificités locales (Nord – Lille, Lambersart, Villeneuve-d''Ascq…)

**Maisons 1930 à radiateurs fonte** : l''inertie est excellente, mais la température de départ peut être élevée en plein hiver.

Si l''isolation n''est pas refaite, envisagez air-eau haute température ou PAC hybride (gaz + PAC) pour garder du confort par grand froid, tout en économisant le reste du temps.

**Humidité et 0–5 °C** : expectez dégivrages plus fréquents en plein hiver. D''où l''importance d''un bon dimensionnement et d''un positionnement du groupe extérieur (écoulement des condensats, flux d''air non recyclé).

**Copropriétés** : la PAC air-air multisplit est souvent choisie pour sa simplicité et la clim l''été, mais l''assemblée générale doit valider l''implantation extérieure (bruit, esthétique, percement de façade/cour).

