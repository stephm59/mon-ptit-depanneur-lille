- **PAC air-eau** (chauffage seul) : 9 000 à 15 000 € posé.
- **PAC air-eau + ECS** (ballon 180–200 L) : 11 000 à 18 000 € posé.
- **Hybride gaz + PAC** : 11 000 à 19 000 € posé, selon chantier.

