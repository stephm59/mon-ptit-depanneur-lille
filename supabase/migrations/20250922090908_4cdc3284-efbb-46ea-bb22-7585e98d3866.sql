## Aides & copro : l''essentiel à retenir (résumé pratique)

**PAC air-eau** : souvent éligible (selon revenus/logement/travaux), notamment via les dispositifs nationaux (à vérifier au moment du devis).

**PAC air-air** : les aides sont plus limitées car on est sur un système "clim réversible" (chauffage électrique).

**Copro** : soumettre le projet en AG (notice technique, niveaux sonores jour/nuit, photos-montage d''implantation, gestion des condensats).

## Bruit & implantation (petites cours lilloises)

- Silentblocs, socle désolidarisé, orientation du souffle loin des fenêtres voisines.
- Écran acoustique si cour réverbérante ; éviter les angles qui "amplifient" le bruit.
- Distance aux limites selon règlement, maintenance accessible (dégivrage, écoulements).

