**Étanchéité bâclée sur l''italienne** : 1 cm de pente "gagné", une fuite "perdue".

**Éclairage trop froid** : ambiance clinique.

**Pas de rangements** : flacons au sol, joints qui vieillissent mal → prévoyez niche / colonne.

