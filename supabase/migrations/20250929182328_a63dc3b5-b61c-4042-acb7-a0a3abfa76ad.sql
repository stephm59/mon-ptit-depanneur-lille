**Douche + baignoire** : idéal si la surface le permet (ou si vous avez 2 pièces d''eau).

**Baignoire-douche** : paroi 2/3, mitigeur thermostatique, barre de maintien = pratique et sécurisé.

**Prévoir l''avenir** : installer une baignoire aujourd''hui (enfants petits) tout en pré-équipant les arrivées/évacuations pour passer à une douche plus tard sans tout casser.

