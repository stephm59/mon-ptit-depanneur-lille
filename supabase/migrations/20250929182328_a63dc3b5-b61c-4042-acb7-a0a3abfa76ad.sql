## La baignoire : confort, enfants et cachet

👉 À partir de 70 × 140 cm, avec du recul pour entrer/sortir.

### Points forts

🛁 **Bien-être** : rien ne remplace un bain chaud après une journée chargée.

👶 **Enfants** : pratique et ludique (bain + jeux).

✨ **Décor** : la baignoire îlot (vintage ou design) devient une pièce maîtresse.

🤲 **Matériaux actuels** : plus doux, chauds au toucher, parfois antibactériens.

### À surveiller

**Emprise au sol** : la baignoire occupe souvent 1/3 de la pièce.

**Accès** : rebord = marche (moins idéal si mobilité réduite).

**Eau** : 150–250 L par bain selon taille/habitudes.

**Astuce plan** : Dans ≥ 5 m² : baignoire adossée au mur long, rangements en tête, éclairage dimmable pour l''ambiance.

