## 2) Le cœur de la sécurité : le cylindre (et ses renforts)

Même sur une bonne serrure, tout se joue souvent au cylindre et à ses protections.

- **Cylindre standard** : basique ; résiste mal au crochetage et au perçage.
- **Cylindre haute sécurité** : goupilles spé, barre anti-casse, pastilles anti-perçage, clé protégée (carte de propriété).
- **Norme/certif A2P (1, 2 ou 3 étoiles)** : plus d''étoiles = résistance certifiée aux attaques (selon ensemble serrure + protecteurs).

### Protections indispensables :

- Rosace blindée / protège-cylindre : empêche l''arrachement.
- Vis traversantes et plaques acier : rigidifient l''ensemble.
- Gâche renforcée / barre de seuil : contre le pied-de-biche.
- Paumelles sécurité / pions anti-dégondage : si la porte s''ouvre vers l''extérieur.

**Règle d''or** : un bon cylindre + protections vaut mieux qu''une "grosse serrure" avec un cylindre faible.

## 3) Comment choisir (sans se perdre) ?

### a) Votre porte

- **Bois/métal** : toutes options (encastrée, multipoints).
- **PVC/aluminium** : attention aux profils ; privilégiez cylindre européen et plaques de renfort compatibles.
- **Porte ancienne ou déformée** : l''en applique est souvent la meilleure base.

### b) Votre usage

- **Résidence principale** : viser multipoints + cylindre sécurisé (A2P si possible).
- **Location / Airbnb** : numérique/connected (codes temporaires) + sécurité mécanique en secours.
- **Bureaux** : contrôle d''accès (badge), traçabilité, sortie facile (normes issues de secours).

### c) Votre niveau de risque

- **Rez-de-chaussée/isolé** : renforts à privilégier (gâche + rosace blindée).
- **Copro étagée** : pied-de-biche plus rare ; soignez le cylindre et la clé protégée (évite les copies sauvages).

### d) Votre budget (repères indicatifs, pose comprise)

- **Remplacement cylindre** : 120 – 350 € selon sécurité (clé protégée incluse).
- **Serrure à encastrer standard** : 180 – 450 €.
- **Multipoints carénée** : 500 – 1 100 €.
- **Serrure connectée de qualité** (sur cylindre existant) : 250 – 600 € (hors blindage).

(Les prix varient selon marque, niveau A2P, travaux d''ajustage, état de la porte.)

## 4) Cas concrets (pour vous situer)

- **Porte d''entrée bois en maison 1930** : multipoints encastrée + cylindre A2P + rosace blindée + gâche acier.
- **Palière en copro (porte isoplane)** : encastrée 3 points + cylindre protégé ; penser au joint de seuil acoustique.
- **Entrée secondaire / atelier** : serrure en applique lourde + renforts visibles (dissuasion).
- **Location courte durée** : serrure à code + cylindre sécurité (clé propriétaire) en secours.

## 5) Clés, copies et… tranquillité d''esprit

- **Clé protégée** : fournie avec une carte de propriété ; copies seulement chez un centre agréé → vous gardez le contrôle.
- **Perte/vol** : changez uniquement le cylindre (pas toute la serrure) si compatible.
- **Emménagement** : remplacez le cylindre (vous ignorez qui a une copie).
- **Entretien** : un simple lubrifiant sec (graphite/PTFE) 1–2 fois/an, pas de graisse collante.

## 6) Sécurité réelle : 4 erreurs à éviter

- **Cylindre qui dépasse de la rosace** → proie facile à l''arrachement.
- **Porte non réglée (frotte, jeu)** → même une multipoints ferme mal = sécurité diminuée.
- **Pari "tout électronique" sans plan B** → gardez une clé mécanique de secours.
- **Oublier les renforts (gâche/plaques)** → le pied-de-biche ne s''occupe pas du cylindre…

## 7) Mini-FAQ express

**La meilleure serrure ?**  
Celle adaptée à votre porte et à votre usage : idéalement multipoints + cylindre A2P + renforts.

**Je n''ai pas le budget pour tout changer ?**  
Commencez par le cylindre haute sécurité + rosace blindée. Gros gain pour un coût maîtrisé.

**Connectée = plus sûre ?**  
C''est surtout plus pratique. Choisissez une marque sérieuse, mettez à jour le firmware et gardez une clé de secours.

**Combien de points faut-il ?**  
En pratique, 3 points bien posés changent tout. 5 ou 7 points ajoutent de la tenue sur portes hautes/à grand jeu.

