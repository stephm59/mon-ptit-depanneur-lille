## 1) Les grandes familles (avec usages typiques)

### Serrure tubulaire (ou "à bouton")

- **Où ?** Portes intérieures (WC, salle d''eau, bureau).
- **Pourquoi ?** Verrouillage rapide de l''intérieur (bouton/pression).
- **+** Simple, économique, installation facile.
- **–** Sécurité très limitée : à éviter pour une porte d''entrée.

### Serrure en applique

- **Où ?** Portes anciennes, portails, entrées secondaires.
- **Pourquoi ?** Boîtier posé en surface (quand on ne peut pas encastrer).
- **+** Robuste, pose plus simple, bonne longévité.
- **–** Visible, donc attaquable au levier si mal protégée (prévoir gâche/platines renforcées).

### Serrure à encastrer (ou "mortaise")

- **Où ?** Porte d''entrée moderne (bois, métal, parfois PVC).
- **Pourquoi ?** Intégrée dans l''âme de la porte.
- **+** Discrète, plus sécurisée, compatible multi-points.
- **–** Pose plus technique (ajustage précis).

### Serrure à cylindre européen (le standard actuel)

- **Où ?** 90 % des portes d''entrée récentes.
- **Pourquoi ?** Un cylindre (canon) actionne le mécanisme ; on le change sans remplacer la serrure.
- **+** Peut recevoir un cylindre haute sécurité (anti-crochetage, anti-perçage, anti-casse/anti-arrachement).
- **–** Sécurité variable selon la qualité du cylindre et des renforts.

### Serrure multipoints (3, 5, 7 points)

- **Où ?** Portes d''entrée, portes palières (copro).
- **Pourquoi ?** Verrouille en plusieurs points (haut/bas/latéral).
- **+** Excellente résistance au pied-de-biche et au flambage.
- **–** À bien régler (porte droite/équerrée) ; prix supérieur.

### Serrure carénée

- **Où ?** Portes d''entrée où l''on veut cacher les tringles.
- **Pourquoi ?** Multipoints sous un capot vertical (esthétique + rigidité).
- **+** Propre et discret, renforce la porte visuellement.
- **–** Plus chère ; pose qualifiée.

### Serrures numériques / connectées (code, carte, smartphone, empreinte)

- **Où ?** Hôtels, bureaux, Airbnb, résidences principales/secondaires.
- **Pourquoi ?** Sans clé (ou clé en secours), journal des accès.
- **+** Confort (codes temporaires, appli), pas de copie de clé à gérer.
- **–** Dépend de piles/alimentation ; la sécurité dépend du modèle et des réglages.

