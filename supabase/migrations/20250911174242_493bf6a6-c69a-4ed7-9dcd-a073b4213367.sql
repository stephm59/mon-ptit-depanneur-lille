## Changer uniquement le cylindre (barillet)

Voici les étapes à suivre :

• **Ouvrez la porte** complètement
• **Repérez la vis de fixation** située sur la têtière (au niveau du panneton)
• **Retirez cette vis** avec un tournevis adapté
• **Introduisez la clé**, tournez-la légèrement pour aligner le cylindre
• **Tirez le cylindre** délicatement pour le sortir
• **Insérez le nouveau cylindre** (avec la clé en place, c''est plus simple)
• **Replacez la vis** et serrez-la fermement

👉 **Temps estimé** : 5 minutes

## Changer toute la serrure

Si vous devez remplacer l''ensemble du mécanisme :

• **Dévissez toutes les vis** de fixation de la serrure
• **Retirez la serrure complète** de son logement
• **Placez la nouvelle serrure** de même taille (elle doit remplir exactement l''espace)
• **Revissez l''ensemble** solidement
• **Testez le bon fonctionnement** de l''ouverture et fermeture

👉 **Temps estimé** : 10 minutes

## Conseils de sécurité

⚠️ **Points importants à retenir :**

• Ne "bricolez" pas avec une serrure trop petite ou trop grande
• La serrure protège votre maison : elle doit être parfaitement adaptée
• Vérifiez toujours le bon fonctionnement avant de fermer définitivement la porte
• Gardez une clé de secours pendant l''installation

