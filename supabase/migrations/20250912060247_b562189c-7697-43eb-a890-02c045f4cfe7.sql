## Conseils pour économiser l''eau

### 🚿 Dans la salle de bain
• Préférez les douches aux bains (une douche = 60L vs un bain = 150L)
• Installez des mousseurs sur les robinets
• Réparez rapidement les fuites

### 🍽️ Dans la cuisine
• Faites tourner le lave-vaisselle seulement quand il est plein
• Récupérez l''eau froide en attendant l''eau chaude
• Lavez les légumes dans un récipient plutôt qu''à l''eau courante

### 🚿 Chasse d''eau et fuites
• Une chasse d''eau qui fuit = jusqu''à 25L/heure !
• Test simple : mettez du colorant alimentaire dans le réservoir, si la cuvette se colore = fuite
• Ajustez le mécanisme ou changez le joint

