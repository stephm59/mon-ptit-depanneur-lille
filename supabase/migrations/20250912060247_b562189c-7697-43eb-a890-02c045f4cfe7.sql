## Comment bien lire votre compteur ?

### Étape 1 : Localisez votre compteur

🔍 **Où le trouver ?**

• **En maison individuelle** : généralement dans le jardin, près de la rue, dans un regard au sol
• **En copropriété** : souvent dans les parties communes (cave, local technique)
• **En appartement** : parfois dans le placard ou la salle de bain

### Étape 2 : Relevez les bons chiffres

⚡ **Règle d''or** : ne relevez QUE les chiffres sur fond noir (ou blancs) !

**Exemple pratique :**
Si vous lisez **0538** sur fond noir et **4256** sur fond rouge :
→ Votre consommation = **538 m³** (on ignore le 0 en début)

### Étape 3 : Calculez votre consommation

📊 **Formule simple :**
Consommation = Index actuel - Index précédent

**Exemple :**
• Index précédent : 520 m³
• Index actuel : 538 m³
• Consommation = 538 - 520 = **18 m³**

