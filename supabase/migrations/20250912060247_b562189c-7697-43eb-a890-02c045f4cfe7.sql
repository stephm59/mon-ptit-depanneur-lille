## Que faire en cas de surconsommation ?

### 🔍 Vérifiez les fuites cachées

**Test du compteur :**
1. Fermez tous les robinets
2. Notez les chiffres du compteur
3. Attendez 2 heures sans utiliser d''eau
4. Relevez à nouveau → si les chiffres ont bougé = fuite !

**Les endroits à vérifier :**
• WC (colorant dans le réservoir)
• Robinets qui gouttent
• Tuyauterie sous les éviers
• Raccords machine à laver / lave-vaisselle

### 📞 Contactez un professionnel

Si vous détectez une fuite ou une anomalie, n''attendez pas !

**Pourquoi agir rapidement ?**
• Une petite fuite peut coûter des centaines d''euros par an
• Les dégâts des eaux peuvent s''aggraver
• Votre assurance peut ne pas couvrir si vous avez tardé à agir

