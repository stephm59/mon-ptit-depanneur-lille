<h2>Désembouage : qu''est-ce qu''on fait exactement ?</h2>

<h3>Méthode pro (ce qu''on fait chez vous)</h3>

<ol>
<li><strong>Diagnostic rapide</strong> : test à la purge, contrôle boues/aimant, état des robinets/collecteurs.</li>
<li><strong>Injection d''un nettoyant spécifique</strong> (biodégradable) et mise en circulation (30–90 min selon réseau).</li>
<li><strong>Rinçage en charge</strong> radiateur par radiateur (ou par boucle plancher), chasse des dépôts.</li>
<li><strong>Pose d''un filtre magnétique</strong> (sur le retour chaudière) pour capturer l''oxyde sur la durée.</li>
<li><strong>Inhibiteur</strong> (anti-corrosion/anti-tartre) à dose.</li>
<li><strong>Équilibrage des débits</strong> + vérifs pression/vase d''expansion.</li>
<li><strong>Remise d''un rapport</strong> (photos, conseils d''entretien).</li>
</ol>

<p><strong>Pourquoi le filtre magnétique ?</strong> Parce qu''il prolonge l''effet du désembouage : il piégera les particules pendant des années. Sans lui, les dépôts… reviennent.</p>

