<h2>Quand désembouer ? (timing malin à Lille)</h2>

<ul>
<li><strong>Avant l''hiver</strong> (sept.–nov.) pour profiter tout de suite du gain.</li>
<li><strong>Après une pose de chaudière/PAC</strong> : un réseau propre protège l''appareil neuf.</li>
<li><strong>Au moindre symptôme</strong> (zones froides, glouglous, pompe bruyante).</li>
<li><strong>Rythme préventif</strong> : tous 5–7 ans en réseau acier/fonte, 8–10 ans en cuivre/multicouche (avec filtre en place).</li>
</ul>

<h2>Après le désembouage : comment garder le réseau propre</h2>

<ul>
<li><strong>Filtre magnétique</strong> : un petit nettoyage (aimant) à chaque entretien chaudière.</li>
<li><strong>Pression</strong> : visez 1,2–1,5 bar à froid ; si ça chute régulièrement → diagnostic.</li>
<li><strong>Équilibrage</strong> : évitez de fermer à fond un radiateur (débits se dérèglent).</li>
<li><strong>Eau</strong> : on peut compléter avec une eau adoucie (selon matériel), et toujours inhiber le circuit.</li>
</ul>

