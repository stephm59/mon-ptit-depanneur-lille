<h2>Un mot sur la PAC air-eau</h2>

<p>Si vous passez du gaz à une PAC air-eau, un réseau emboué fausse la loi d''eau (la courbe de chauffe) et dégradera la perf. Désembouer + filtrer au moment du changement, c''est clé pour un bon COP et un chauffage silencieux.</p>

