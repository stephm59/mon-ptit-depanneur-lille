| Espace nécessaire | Faible à moyen (UI + UE) | Plus conséquent (UE + hydraulique + ballon) |
| Complexité | Modérée | Élevée (hydraulique, permissions) |
| Cas typique en copro | Le plus courant | Plus rare |

En pratique à Lille, on voit majoritairement des multi-splits air-air pour le chauffage mi-saison + clim d''été, notamment en duplex et combles aménagés.

## Le cadre copro en 5 étapes (simple et efficace)

### 1. Vérifier le règlement de copropriété

Percements de façade, pose en cour d''îlot, aspects visuels, niveaux sonores : ce sont les points sensibles.

### 2. Concevoir l''implantation (avant de demander le vote)

- **Emplacement unité extérieure (UE)** : cour, toiture-terrasse technique, balcon solide (éviter garde-corps légers).
- **Orientation du souffle** loin des fenêtres voisines ; dégagement suffisant (pas d''air recyclé).
- **Désolidarisation** : silentblocs + socle/châssis antivibratile.
- **Condensats** : tracé discret vers un drain (évite bruit d''égouttement/glace l''hiver).

### 3. Monter le dossier d''AG

- Fiches techniques (UE/UI) avec puissance et niveaux sonores jour/nuit.
- Plan/photomontage d''implantation sur façade/cour + schéma des condensats.
- Note d''un installateur RGE indiquant moyens d''atténuation (silentblocs, écran absorbant).
- Engagement d''entretien annuel et d''intervention en cas de nuisance.

### 4. Vote en AG

Présentation synthétique (3–4 pages). Plus c''est clair, plus ça passe.

Conservez le PV d''AG avec l''autorisation.

### 5. Travaux & réception

- Percements propres, passe-câbles étanches, cheminements discrets.
- Test mode nuit / bas régime et vérification absence de vibration parasite.
- Remise d''un rapport de fin de chantier (photos, niveaux sonores constructeur, consignes d''usage).

## Bonnes pratiques en milieu urbain (Lille & cœur d''îlot)

- **Éviter les angles** : ils "amplifient" le bruit par réverbération.
- **Écran acoustique absorbant** (pas juste décoratif) si vis-à-vis proche.
- **Socle désolidarisé** du bâti ; pas de fixation rigide sur un garde-corps.
- **Accès maintenance** : filtre propre = moins de bruit, meilleure durée de vie.
- **Mode nuit** (si dispo) + plages horaires adaptées.

## Budget indicatif (pour se repérer)

- **Air-air multi-split (2–3 UI)** : 3 500 à 8 500 € posé (marque/configuration).
- **Écran acoustique absorbant** : 400 à 1 500 € selon matériau/dimensions.
- **Châssis antivibratile + silentblocs** : 150 à 600 €.

Les prix varient selon accès chantier, hauteur, nacelle, longueur des liaisons, contraintes de façade/copro.

## Et si je suis locataire ?

- **Accord écrit du propriétaire** avant toute démarche en copro.
- Le bailleur présente (ou autorise le mandataire à présenter) le dossier en AG.

## Check-list "dossier d''AG" (copiez-collez)

