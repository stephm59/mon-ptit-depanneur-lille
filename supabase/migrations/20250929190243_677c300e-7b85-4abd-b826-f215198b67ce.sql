## Qu''est-ce qu''une climatisation réversible ?

Une "réversible" est une pompe à chaleur air-air qui alterne deux modes :

**Mode chauffage** : elle capte les calories de l''air extérieur et les restitue à l''intérieur.

**Mode climatisation** : elle extrait la chaleur de vos pièces et la rejette dehors.

### Les composants

**Une unité extérieure** : c''est le « cœur » qui échange la chaleur avec l''air.

**Une ou plusieurs unités intérieures** : murales, consoles (basse hauteur), gainables (quasi invisibles), parfois cassettes plafonnières.

**Idée clé** : au lieu de "créer du chaud ou du froid", la PAC déplace la chaleur. C''est ce qui explique son bon rendement énergétique.

## Pourquoi choisir une clim réversible ?

✅ **Économies d''énergie** : par rapport à des radiateurs électriques "purs", la PAC air-air consomme moins d''électricité pour un même confort.

✅ **Confort toute l''année** : chauffage + clim + déshumidification en été, très agréable lors d''épisodes humides.

✅ **Gain de place** : un seul système pour deux usages.

✅ **Confort moderne** : réglage précis, programmation, modèles connectés (smartphone, scénarios).

✅ **Budget d''accès** : plus abordable à l''installation qu''une PAC air-eau.

## Les limites à connaître (honnêtement)

🧊 **Par grand froid**, la puissance baisse et le compresseur peut dégivrer : il faut un bon dimensionnement et des attentes réalistes.

🔇 **Bruit** : une mauvaise implantation (extérieur en cour réverbérante, intérieur mal orienté) peut gêner. Les silentblocs et un bon emplacement font toute la différence.

🧽 **Entretien** : filtres à dépoussiérer régulièrement, contrôle pro conseillé pour garder les perfs.

🧱 **Isolation** : si le logement est très peu isolé, la PAC "travaille trop" → commencez par traiter les gros ponts thermiques quand c''est possible.

## Les types d''installations (et pour qui)

- **Mono-split** (1 unité intérieure + 1 extérieure) : idéal pour une pièce de vie principale.
- **Multi-split** (2 à 5 unités intérieures) : pour plusieurs pièces avec une seule unité extérieure.
- **Mural** : le plus courant, rapide à poser.
- **Console** : en bas de mur, pratique sous une fenêtre (remplace un radiateur).
- **Gainable** : quasi invisible, bouche(s) discrète(s) → nécessite faux-plafond.

## Comment choisir la bonne clim réversible ?

### Surface & volumes

On dimensionne sur la pièce à traiter (surface et hauteur). Une sous-puissance = appareil bruyant et inconfort.

### Isolation & exposition

Orientation, vitrage, combles… plus c''est isolé, plus la PAC est silencieuse et stable.

### Niveau sonore

Comparez les dB(A) intérieur/extérieur. Installez l''unité extérieure loin des chambres et désolidarisée du mur (silentblocs).

### Classe saisonnière

Regardez SEER/SCOP (rendement estival/hivernal) et l''étiquette A+, A++… plus c''est haut, mieux c''est.

### Esthétique & pose

Visible (mural/console) ou invisible (gainable) ? Cheminement des liaisons, évacuation condensats à prévoir.

### Fonctions utiles

Connectivité (pilotage à distance), capteurs de présence, mode silence nuit, déshu renforcée, filtres améliorés.

## "Clim réversible" vs "pompe à chaleur air-eau" : quelle différence ?

- **La clim réversible = PAC air-air** : elle souffle de l''air chaud/froid dans la pièce.
- **La PAC air-eau** alimente des radiateurs/planchers et peut produire l''ECS.

👉 La réversible est moins chère à l''installation, très confortable pièce par pièce, mais ne remplace pas un système central quand on veut chauffer tout le logement par eau chaude.

## Bon placement = confort + silence

**Unité extérieure** : hors zone de passage, dégagée pour l''air, sur silentblocs. Attention aux cours intérieures qui résonnent : prévoyez un support désolidarisé et, si besoin, un écran acoustique ventilé.

**Unités intérieures** : éviter au-dessus du canapé ou face au lit. Privilégier une diffusion libre (pas cachée par un meuble).

## Entretien simple (et utile)

**Toutes les 2–4 semaines en saison** : aspirer/rincer les filtres des unités intérieures.

**Chaque année** : contrôle pro conseillé (nettoyage échangeurs, condensats, vérifs électriques), surtout si l''appareil tourne beaucoup.

**Bon réflexe** : garder une photo de l''installation (plaques signalétiques, longueurs, passages), pratique en cas de SAV.

## Questions fréquentes

**Est-ce que ça assèche l''air ?**  
La réversible déshumidifie en été ; en hiver, utilisez un mode doux et évitez de surchauffer l''air.

**Puis-je chauffer tout mon logement avec une PAC air-air ?**  
Oui si vous équipez plusieurs pièces (multi-split). Pour un chauffage intégral par eau chaude, voyez plutôt la PAC air-eau.

**Et l''hiver quand il gèle ?**  
Les bons modèles chauffent encore par températures négatives, mais la puissance utile baisse et les cycles de dégivrage apparaissent : d''où l''intérêt d''un dimensionnement sérieux.

**Que regarder sur l''étiquette ?**  
La classe (A+, A++…), les SEER/SCOP, le niveau sonore, et les modes silence.

