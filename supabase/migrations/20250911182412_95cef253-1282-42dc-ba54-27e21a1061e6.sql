👉 Voici comment identifier un problème, comprendre son origine et savoir quoi faire.

## Les signes d''un mitigeur défectueux

### 🌡️ Problème de température :
• L''eau reste trop froide ou trop chaude
• La température ne correspond pas au réglage choisi
• Variations brusques de température

### 💧 Débit d''eau faible :
• Le jet est réduit ou irrégulier
• Signe possible de tartre ou d''obstruction interne
• Pression insuffisante à l''ouverture

### 🚰 Fuite visible :
• De l''eau s''échappe au niveau du bec
• Fuite à la base du mitigeur
• Gouttes persistantes même robinet fermé

⚠️ **Dans la majorité des cas, le problème vient de la cartouche thermostatique, pièce centrale du mitigeur.**

## Pourquoi un mitigeur tombe-t-il en panne ?

### 🪨 Calcaire et tartre :
• Dépôts qui bloquent la circulation de l''eau
• Accumulation progressive dans les conduits
• Réduction du débit et des performances

### 🧹 Saletés et impuretés :
• Particules qui s''incrustent dans les mécanismes
• Obstruent les passages internes
• Altèrent le fonctionnement de la cartouche

### 🔧 Manque d''entretien :
• Sans nettoyage régulier, la cartouche s''use plus vite
• Joints qui se détériorent
• Mécanismes qui se grippent

👉 **Résultat :** température instable, débit réduit ou fuites.

## Entretien de la cartouche thermostatique

Un simple nettoyage régulier peut prolonger la durée de vie du mitigeur :

### 🔧 Étapes de démontage :
• Coupez l''arrivée d''eau générale
• Purgez le mitigeur en levant la manette pour évacuer l''eau
• Retirez les caches décoratifs
• Dévissez les écrous avec une clé Allen

### 🧽 Nettoyage de la cartouche :
• Utilisez du vinaigre blanc ou un produit anticalcaire
• Laissez tremper si nécessaire
• Brossez délicatement les dépôts
• Rincez abondamment à l''eau claire

### 🔄 Remontage :
• Remontez dans l''ordre inverse
• Vérifiez l''étanchéité de tous les joints
• Testez le bon fonctionnement

👉 **Si le problème persiste, il faudra remplacer la cartouche, voire changer le mitigeur.**

