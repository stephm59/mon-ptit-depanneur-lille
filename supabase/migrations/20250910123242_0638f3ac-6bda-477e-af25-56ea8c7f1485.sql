## Astuce 1 : Le fil de fer plié en crochet

Dépliez un cintre métallique et façonnez un crochet à l''extrémité. Insérez-le dans la canalisation pour retirer cheveux, graisses ou petits déchets. Finissez en versant de l''eau bouillante pour rincer.

## Astuce 2 : L''eau bouillante

Simple et écologique ! Faites bouillir de l''eau et versez-la en plusieurs fois dans la canalisation. Ce geste peut dissoudre graisses et résidus accumulés. À répéter régulièrement en prévention.

## Astuce 3 : Le mélange sel + vinaigre + bicarbonate

Un grand classique du ménage maison 💡.

Mélangez ¾ de verre de bicarbonate + ½ verre de vinaigre.

Versez dans la canalisation et bouchez avec un chiffon.

Laissez agir 30 min, puis rincez à l''eau bouillante.

Résultat : un déboucheur naturel, sans produits chimiques agressifs.

## Astuce 4 : Le liquide vaisselle + eau chaude

La graisse de cuisine bouche souvent les tuyaux. Versez ¼ de tasse de produit lave-vaisselle, puis de l''eau bouillante. Pour plus d''efficacité, ajoutez un peu de levure chimique.

