## ❓ FAQ

### Pourquoi mes canalisations se bouchent-elles souvent ?

Les causes les plus fréquentes : cheveux, graisses, restes alimentaires, lingettes et dépôts de calcaire.

### L''eau bouillante suffit-elle à déboucher un évier ?

Oui pour de petits dépôts gras. Mais si le bouchon est important, combinez avec du bicarbonate ou un furet.

### Faut-il utiliser de la soude caustique ?

Uniquement en dernier recours et avec des protections, car c''est un produit dangereux.

### Comment prévenir les bouchons ?

Évitez de jeter graisses et déchets dans l''évier.

Placez des filtres dans les bondes.

Versez régulièrement de l''eau chaude pour dissoudre les graisses.

### Quand appeler un plombier ?

Si après 2-3 tentatives le bouchon persiste, ou si plusieurs canalisations sont bouchées en même temps (signe d''un problème plus grave).

