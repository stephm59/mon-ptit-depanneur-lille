## 1. Sécurité avant tout : les premiers réflexes

**Dès que vous constatez une vitre cassée, la priorité absolue est la sécurité des occupants.**

### Évacuer la zone immédiate
- **Éloignez les enfants et les animaux** de la zone dangereuse
- Interdisez l''accès à la pièce si possible
- Allumez les lumières pour bien voir les éclats de verre

### Protégez-vous avant d''intervenir
- **Portez des chaussures fermées** (jamais pieds nus !)
- Utilisez des **gants épais** (gants de jardinage, gants en cuir)
- Portez des **manches longues** pour protéger vos bras
- Envisagez des **lunettes de protection** si vous devez manipuler de grands morceaux

### Attention aux situations particulières
- **Si la vitre est fissurée mais pas encore tombée** : ne la touchez pas, elle pourrait s''effondrer
- **En cas d''effraction** : n''entrez pas, appelez immédiatement la police
- **Si quelqu''un est blessé** : contactez les secours (15 ou 18) en priorité

💡 **Conseil pratique** : Prenez des photos de la vitre cassée dès que possible pour votre déclaration d''assurance.

## 2. Nettoyer les débris de verre en toute sécurité

**Le nettoyage des éclats de verre est une étape délicate qui demande de la méthode.**

### Pour les gros morceaux
1. **Ramassez les plus gros fragments** avec des gants épais
2. Placez-les dans un **carton épais** ou une boîte rigide (jamais dans un sac plastique fin)
3. Marquez clairement le carton "VERRE - DANGER"

### Pour les petits éclats
1. **Utilisez un balai** pour regrouper les débris vers un point central
2. **Aspirez soigneusement** toute la zone (y compris les plinthes et les recoins)
3. **Passez un chiffon humide ou du ruban adhésif** sur le sol pour capturer les micro-fragments invisibles
4. N''oubliez pas de nettoyer **à l''extérieur** si des éclats ont été projetés dehors

### Élimination des débris
- Emballez le tout dans plusieurs couches de papier journal
- Placez dans un carton bien fermé
- Informez les éboueurs ou amenez en déchetterie selon les consignes locales
- **Ne jetez jamais du verre cassé** dans un sac poubelle ordinaire

