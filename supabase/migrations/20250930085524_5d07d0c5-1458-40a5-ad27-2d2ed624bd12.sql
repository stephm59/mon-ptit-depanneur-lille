## 7. Prévenir les risques de casse à l''avenir

**Quelques précautions simples permettent d''éviter les vitres cassées.**

### Protections physiques
- **Film de protection anti-effraction** : renforce la résistance
- **Barres ou grilles** : pour les rez-de-chaussée
- **Volets ou persiennes** : protection mécanique efficace
- **Garde-corps** : protègent des chocs accidentels

### Précautions au quotidien
- Évitez de claquer les portes violemment
- Signalez les vitres avec des autocollants visibles (surtout les baies vitrées)
- Surveillez les jeux d''enfants à proximité des fenêtres
- Taillez les arbres et arbustes qui pourraient heurter les vitres par vent fort

### Entretien préventif
- **Inspectez régulièrement** le mastic et les joints
- Remplacez les vitrages fissurés ou fragilisés
- Vérifiez que les cadres sont bien fixés
- Entretenez les systèmes d''ouverture pour éviter les tensions

### Cas particuliers
- **Zones à risque (ballons, sports)** : préférez du verre trempé ou feuilleté
- **Façades exposées** : envisagez du vitrage renforcé
- **Régions venteuses** : vérifiez la bonne tenue des vitrages

