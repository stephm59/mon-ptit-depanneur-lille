## 3. Solutions temporaires pour colmater l''ouverture

**Une fois la zone nettoyée, il faut rapidement protéger l''ouverture.**

### Option 1 : Le film plastique épais (solution rapide)
**Matériel nécessaire :**
- Bâche plastique ou film polyane épais
- Ruban adhésif renforcé type "duct tape"
- Ciseaux

**Procédure :**
1. Mesurez l''ouverture en ajoutant 15-20 cm de chaque côté
2. Découpez le plastique aux dimensions
3. Fixez solidement sur le cadre avec du ruban adhésif
4. Vérifiez que le plastique ne bat pas au vent

**Avantages :** Rapide, peu coûteux, laisse passer la lumière
**Inconvénients :** Peu esthétique, ne protège pas du froid

### Option 2 : Le panneau de contreplaqué (solution solide)
**Matériel nécessaire :**
- Panneau de contreplaqué ou planche en bois
- Vis ou clous
- Perceuse ou marteau
- Équerre pour mesurer

**Procédure :**
1. Mesurez précisément l''ouverture
2. Découpez le panneau (ou faites-le découper en magasin)
3. Fixez solidement sur le cadre de la fenêtre
4. Comblez les espaces avec du mastic si nécessaire

**Avantages :** Très sécurisé, bonne isolation, protection contre les intrusions
**Inconvénients :** Obscurcit totalement, nécessite plus de matériel

### Option 3 : Le carton renforcé (dépannage temporaire)
**Matériel nécessaire :**
- Plusieurs cartons épais (type cartons de déménagement)
- Ruban adhésif large
- Cutter

**Procédure :**
1. Superposez 2-3 épaisseurs de carton
2. Découpez aux dimensions
3. Fixez avec du ruban adhésif renforcé
4. Protégez avec un sac plastique à l''extérieur

**Avantages :** Matériel souvent disponible à la maison, rapide
**Inconvénients :** Solution très temporaire (quelques heures maximum), peu résistant

💡 **Important** : Quelle que soit la solution choisie, vérifiez régulièrement sa tenue, surtout en cas de vent ou de pluie.

## 4. Cas particuliers selon le type de vitrage

**Tous les vitrages ne réagissent pas de la même façon lorsqu''ils se brisent.**

### Double vitrage cassé
- **Si seul le vitrage extérieur est cassé** : le vitrage intérieur maintient temporairement l''isolation
- **Si les deux vitrages sont cassés** : colmatez rapidement pour éviter les pertes d''énergie importantes
- **Attention** : L''eau ou l''humidité entre les deux vitres indique que le double vitrage a perdu son étanchéité

### Verre feuilleté (sécurité)
- Les morceaux restent généralement collés au film plastique central
- **Ne retirez pas le film** : il maintient la structure
- Le vitrage peut rester en place quelques heures sans danger immédiat
- Évitez tout choc supplémentaire qui pourrait faire céder la structure

### Verre trempé
- Se brise en milliers de petits morceaux aux bords peu coupants
- **Tout le panneau tombe d''un coup** lorsqu''il se brise
- Nettoyez minutieusement car les fragments sont partout
- Remplacement obligatoire : impossible à réparer

### Vitrail ou verre ancien
- Manipulez avec une extrême précaution : irremplaçable
- **Conservez tous les morceaux**, même les plus petits
- Contactez un spécialiste de la restauration de vitraux
- Ne tentez aucune réparation temporaire qui pourrait endommager davantage

## 5. Assurance : les démarches à effectuer

**Une vitre cassée est généralement couverte par votre assurance habitation.**

### Ce qui est généralement couvert
- **Bris de glace** : garantie spécifique dans la plupart des contrats
- **Vandalisme ou effraction** : avec dépôt de plainte obligatoire
- **Catastrophe naturelle** : tempête, grêle (franchise applicable)
- **Accident domestique** : selon les termes de votre contrat

### Démarches à suivre
1. **Déclarez le sinistre rapidement** : généralement sous 5 jours ouvrés
2. **Prenez des photos** : de la vitre cassée et des dégâts éventuels
3. **Conservez les preuves** : devis, factures, constats
4. **Demandez un numéro de sinistre** pour le suivi de votre dossier

### Documents à préparer
- Numéro de contrat d''assurance
- Photos des dégâts
- Devis du vitrier
- Constat de police (en cas d''effraction)
- Facture après intervention

### Ce qui reste à votre charge
- **La franchise** : montant variable selon votre contrat (souvent entre 50€ et 150€)
- Les dégâts collatéraux non couverts
- La protection temporaire si vous faites appel à un service payant

