## 6. Quand faut-il appeler un vitrier en urgence ?

**Certaines situations nécessitent une intervention immédiate, même en pleine nuit.**

### Urgences absolues (intervention sous 2h)
- **Effraction avec vitre cassée** : risque de récidive
- **Vitrage extérieur complètement absent** : exposition aux intempéries
- **Rez-de-chaussée ou accès facile** : risque d''intrusion
- **Commerce ou local professionnel** : sécurité des biens
- **Conditions météo extrêmes** : pluie battante, tempête, grand froid

### Interventions rapides (sous 24h)
- **Double vitrage partiellement cassé** : perte d''isolation
- **Vitre de salle de bain cassée** : perte d''intimité
- **Porte-fenêtre ou baie vitrée** : grande ouverture
- **Étages supérieurs** : moins de risque mais nécessite protection

### Peut attendre quelques jours
- **Vitrage secondaire cassé** (cave, garage)
- **Petit carreau avec protection temporaire efficace**
- **Fenêtre peu accessible** avec colmatage solide
- **Bâtiment inoccupé** sans risque particulier

