### 3. 🎛️ Thermostat mal réglé ou défectueux

**Symptômes :**
- La température affichée ne correspond pas à la réalité
- Cycles de marche/arrêt très courts
- La clim s''arrête avant d''avoir refroidi
- Température inconstante dans la pièce

**Pourquoi ça arrive :**  
Mauvais emplacement du thermostat, réglages inadaptés, ou dysfonctionnement du capteur de température.

**✅ Solution simple :**
1. Vérifiez que le thermostat n''est pas exposé au soleil direct
2. Descendez la température cible de 2-3°C
3. Passez en mode "refroidissement" (et non "automatique")
4. Changez les piles si c''est un thermostat sans fil
5. Réinitialisez le thermostat (voir manuel)

**Réglage optimal en été :** 24-26°C (pas moins de 6-7°C d''écart avec l''extérieur pour éviter le choc thermique)

