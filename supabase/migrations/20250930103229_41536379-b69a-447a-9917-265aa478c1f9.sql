**Après l''été (septembre-octobre) :**
- Nettoyage approfondi avant hivernage
- Protection de l''unité extérieure si nécessaire
- Vérification de l''étanchéité

**Entretien professionnel annuel :** Obligatoire tous les 2 ans si puissance >12kW, recommandé tous les ans pour tous les modèles.

