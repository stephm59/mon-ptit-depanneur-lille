### 2. 🌿 Unité extérieure obstruée

**Symptômes :**
- Le ventilateur extérieur force et fait du bruit
- L''unité extérieure est très chaude au toucher
- Arrêts fréquents par surchauffe
- Performances réduites progressivement

**Pourquoi ça arrive :**  
Feuilles mortes, poussière, toiles d''araignée, végétation qui a poussé trop près... L''unité extérieure a besoin de respirer pour évacuer la chaleur.

**✅ Solution simple :**
1. Coupez l''alimentation électrique
2. Retirez les débris visibles (feuilles, branches)
3. Nettoyez délicatement les ailettes avec un jet d''eau (pas de nettoyeur haute pression !)
4. Taillez la végétation autour (minimum 50 cm de dégagement)
5. Vérifiez que les grilles ne sont pas obstruées

**Astuce pro :** Un simple nettoyage de l''unité extérieure peut restaurer jusqu''à 20% de performance perdue.

