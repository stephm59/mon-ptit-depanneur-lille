## Les 7 causes principales (et leurs solutions)

### 1. ⚡ Filtre à air encrassé (la cause N°1)

**Symptômes :**
- Débit d''air très faible
- Mauvaises odeurs au démarrage
- Surconsommation électrique
- L''air n''est pas assez froid

**Pourquoi ça arrive :**  
Les filtres retiennent poussière, pollens et particules. Un filtre bouché empêche l''air de circuler correctement, ce qui réduit drastiquement l''efficacité de refroidissement.

**✅ Solution simple :**
1. Éteignez votre climatisation
2. Ouvrez le capot de l''unité intérieure
3. Retirez le filtre (généralement accessible sans outil)
4. Nettoyez-le à l''eau tiède savonneuse ou passez l''aspirateur
5. Laissez sécher complètement avant de remettre en place

**Fréquence recommandée :** Nettoyage tous les 15 jours en période d''utilisation intensive, remplacement tous les 3-6 mois.

