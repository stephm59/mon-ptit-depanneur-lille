### 4. ❄️ Fuite de réfrigérant (fluide frigorigène)

**Symptômes :**
- Présence de givre sur les tuyaux ou l''unité intérieure
- Bulles ou traces d''huile sur les raccords
- Perte progressive d''efficacité sur plusieurs semaines
- Sifflements ou gargouillements anormaux

**Pourquoi ça arrive :**  
Corrosion, vibrations, mauvaise installation... Les fuites de gaz réfrigérant sont insidieuses car progressives.

**⚠️ Attention :**  
La manipulation du réfrigérant est **strictement réglementée** et nécessite une habilitation professionnelle. N''essayez JAMAIS de recharger vous-même.

**✅ Ce que vous pouvez faire :**
1. Inspection visuelle des tuyaux et raccords
2. Notez les traces d''huile (révélatrices de fuite)
3. Prenez des photos pour le technicien
4. Appelez un professionnel certifié

**Coût moyen d''une recharge :** 150-400€ selon le type de fluide et la quantité nécessaire.

---

### 5. ⚡ Problème électrique

**Symptômes :**
- Disjonctions fréquentes
- Le ventilateur tourne mais le compresseur ne démarre pas
- Voyants qui clignotent de façon anormale
- Fusibles grillés

**Pourquoi ça arrive :**  
Surcharge électrique, câblage défectueux, condensateur HS, ou compresseur en fin de vie.

**✅ Vérifications sécurisées :**
1. Vérifiez que le disjoncteur n''a pas sauté
2. Inspectez les fusibles du tableau électrique
3. Contrôlez que la prise est bien alimentée (testez avec un autre appareil)
4. Vérifiez l''état des câbles visibles (pas de brûlures, coupures)

**⚠️ Si vous n''êtes pas sûr, appelez un électricien.** Les climatiseurs utilisent du 230V, c''est dangereux.

---

### 6. 🔧 Condensateur défaillant

**Symptômes :**
- Le ventilateur extérieur ne démarre pas
- Bourdonnement au démarrage
- L''unité extérieure est chaude mais immobile
- Le compresseur peine à démarrer

**Pourquoi ça arrive :**  
Le condensateur donne l''impulsion électrique nécessaire au démarrage du moteur. Avec le temps et les cycles thermiques, il s''use.

**✅ Diagnostic simple :**
1. Éteignez la climatisation
2. Essayez de lancer manuellement le ventilateur avec un bâton (ATTENTION : coupez l''alimentation avant !)
3. Si le ventilateur tourne ensuite tout seul, c''est probablement le condensateur

**Coût du remplacement :** 80-150€ pièce et main d''œuvre. C''est une réparation courante et rapide.

---

### 7. 📏 Climatisation mal dimensionnée

**Symptômes :**
- Cycles très courts (moins de 10 minutes)
- Taux d''humidité élevé malgré la clim
- Surconsommation électrique
- Certaines zones restent chaudes

**Pourquoi ça arrive :**  
Une clim trop puissante refroidit trop vite sans déshumidifier. Une clim sous-dimensionnée tourne en continu sans jamais atteindre la température souhaitée.

**✅ Vérification rapide :**
- **Puissance nécessaire :** environ 100W par m² (variable selon isolation, exposition, hauteur sous plafond)
- **Exemple :** Pour 25m², il faut environ 2500W (9000 BTU)

**Solutions :**
- Si surdimensionnée : ajustez les réglages, divisez les zones de refroidissement
- Si sous-dimensionnée : installez une unité supplémentaire ou remplacez par un modèle adapté

