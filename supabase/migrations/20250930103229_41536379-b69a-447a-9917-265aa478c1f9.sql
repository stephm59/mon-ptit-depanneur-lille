## 🔥 Cas particulier : forte chaleur et canicule

**Limites de performance :**  
Une climatisation standard ne peut généralement refroidir que de **10 à 12°C en dessous de la température extérieure**. Si dehors il fait 38°C, n''espérez pas descendre à 22°C.

**Optimisations par forte chaleur :**
1. Fermez volets et rideaux côté soleil
2. Utilisez des ventilateurs pour brasser l''air
3. Évitez d''ouvrir les fenêtres pendant la journée
4. Réglez sur 26-27°C plutôt que 22°C
5. Faites fonctionner la nuit pour refroidir l''inertie du logement

**Astuce :** En cas de canicule, la climatisation travaille 2 à 3 fois plus. Un manque d''entretien se fera immédiatement sentir.

