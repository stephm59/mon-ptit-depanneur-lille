Pas facile de tout caser : douche, rangements, miroir… sans avoir l''impression d''être à l''étroit. Mais avec les bonnes astuces, même 3 m² peuvent devenir un espace fonctionnel et agréable ! Voici 10 conseils malins pour optimiser chaque centimètre de votre petite salle de bains.

## 1. Optez pour une douche à l''italienne plutôt qu''une baignoire

La baignoire prend beaucoup de place. En la remplaçant par une douche à l''italienne, vous gagnez de précieux mètres carrés et donnez une impression d''espace ouvert.

**Bonus** : sans bac ni porte, la douche à l''italienne agrandit visuellement la pièce.

💡 **Conseil** : choisissez une paroi de douche en verre transparent pour maximiser la luminosité.

## 2. Installez des meubles suspendus

Un meuble vasque suspendu libère le sol et donne une sensation d''espace aéré. En plus, ça facilite le ménage !

👉 Pensez aux tiroirs plutôt qu''aux portes : on accède plus facilement au contenu sans encombrer l''espace devant le meuble.

## 3. Multipliez les rangements muraux

Dans une petite salle de bains, chaque centimètre de mur compte.

- Étagères murales au-dessus des toilettes.
- Patères pour les serviettes.
- Petits paniers suspendus.
- Armoire miroir avec rangements intégrés.

Utilisez la hauteur pour désencombrer le sol et les plans de travail.

## 4. Choisissez un grand miroir pour agrandir l''espace

Un miroir mural de grande taille (voire sur toute la hauteur du mur) réfléchit la lumière et donne l''illusion d''une pièce deux fois plus grande.

**Astuce pro** : placez-le en face d''une source de lumière (fenêtre ou applique) pour maximiser l''effet.

## 5. Privilégiez les couleurs claires et la lumière

Les teintes sombres rétrécissent visuellement l''espace. À l''inverse :

- Blanc, beige, gris clair agrandissent la pièce.
- Carrelage brillant reflète la lumière.
- Multiplier les points lumineux (spots, appliques) évite les zones d''ombre.

## 6. Remplacez la porte battante par une porte coulissante

Une porte classique nécessite un espace de débattement. Une porte coulissante (ou une porte pliante) libère cet espace et permet d''installer des rangements ou un meuble à cet endroit.

## 7. Installez des WC suspendus

Comme pour le meuble vasque, des toilettes suspendues libèrent le sol et facilitent l''entretien. Le bâti-support est encastré dans le mur : gain de place garanti.

## 8. Créez une niche de rangement intégrée

Si vous refaites votre salle de bains, profitez-en pour intégrer une niche dans le mur de la douche ou au-dessus de la baignoire. C''est l''endroit idéal pour ranger gel douche, shampoings… sans ajouter d''étagère qui encombre.

## 9. Choisissez des équipements compacts

Il existe aujourd''hui des lavabos extra-fins, des douches d''angle, des radiateurs sèche-serviettes plats… tous conçus pour les petits espaces.

👉 Pensez aussi aux combinés lavabo + WC pour les très petites surfaces.

## 10. Misez sur le carrelage grand format

Contre-intuitif mais vrai : un grand carrelage (60x60 cm ou plus) avec peu de joints donne une impression d''espace plus vaste qu''un petit carrelage avec de nombreuses lignes de joints.

